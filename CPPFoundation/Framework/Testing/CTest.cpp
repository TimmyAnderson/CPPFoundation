//----------------------------------------------------------------------------------------------------------------------
#include "CTest.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Testing
{
//----------------------------------------------------------------------------------------------------------------------
	using namespace CPPFoundation::Foundation;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CTest::CTest(CLogWriter& LogWriter, const CLogCategory& LogCategoryTest, const CString& Name)
		: MLogWriter(LogWriter), MLogCategoryTest(LogCategoryTest), MName(Name)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CTest::~CTest(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CString CTest::GetSeparator(bool AddNewLine) const
	{
		CString													Value;

		Value+=CString().PadRight(79,'-');

		if (AddNewLine==true)
		{
			Value+='\n';
		}

		return(Value);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CLogWriter& CTest::GetLogWriter(void) noexcept
	{
		return(MLogWriter);
	}
//----------------------------------------------------------------------------------------------------------------------
	const CLogCategory& CTest::GetLogCategoryTest(void) const noexcept
	{
		return(MLogCategoryTest);
	}
//----------------------------------------------------------------------------------------------------------------------
	const CString& CTest::GetName(void) const noexcept
	{
		return(MName);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
