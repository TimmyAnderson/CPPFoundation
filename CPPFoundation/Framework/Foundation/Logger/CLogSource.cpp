//----------------------------------------------------------------------------------------------------------------------
#include "CLogSource.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CLogSource::CLogSource(const CString& SourceType)
		: MSourceType(SourceType)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CLogSource::CLogSource(CString&& SourceType)
		: MSourceType(FUNCTION_MOVE(SourceType))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CLogSource::CLogSource(const CLogSource& Other)
		: MSourceType(Other.MSourceType)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CLogSource::CLogSource(CLogSource&& Other)
		: MSourceType(FUNCTION_MOVE(Other.MSourceType))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CLogSource::~CLogSource(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CLogSource& CLogSource::operator=(const CLogSource& Other)
	{
		MSourceType=Other.MSourceType;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	CLogSource& CLogSource::operator=(CLogSource&& Other)
	{
		MSourceType=FUNCTION_MOVE(Other.MSourceType);

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CLogSource::operator==(const CLogSource& Other) const
	{
		if (MSourceType==Other.MSourceType)
		{
			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CLogSource::operator!=(const CLogSource& Other) const
	{
		if (MSourceType!=Other.MSourceType)
		{
			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	const CString& CLogSource::GetSourceType(void) const noexcept
	{
		return(MSourceType);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
