//----------------------------------------------------------------------------------------------------------------------
#include "CCacheEntry.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CCacheEntry::CCacheEntry(const CCacheEntryID& ID, const CExpirationTime& ExpirationTime)
		: MID(ID), MExpirationTime(ExpirationTime)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CCacheEntry::CCacheEntry(CCacheEntryID&& ID, const CExpirationTime& ExpirationTime)
		: MID(FUNCTION_MOVE(ID)), MExpirationTime(ExpirationTime)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CCacheEntry::~CCacheEntry(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	const CCacheEntryID& CCacheEntry::GetID(void) const noexcept
	{
		return(MID);
	}
//----------------------------------------------------------------------------------------------------------------------
	const CExpirationTime& CCacheEntry::GetExpirationTime(void) const noexcept
	{
		return(MExpirationTime);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
