//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <Framework/Foundation/Strings/CString.h>
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	enum class ESocketDNSAddressType : uint8
	{
//----------------------------------------------------------------------------------------------------------------------
		E_UNSPECIFIED=0x00,
		E_IP_V4=0x01,
		E_IP_V6=0x02,
//----------------------------------------------------------------------------------------------------------------------
	};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline CString& operator<<(CString& Text, ESocketDNSAddressType Value)
	{
		switch(Value)
		{
			case ESocketDNSAddressType::E_UNSPECIFIED:
			{
				Text+="UNSPECIFIED";
			}
			break;

			case ESocketDNSAddressType::E_IP_V4:
			{
				Text+="IP V4";
			}
			break;

			case ESocketDNSAddressType::E_IP_V6:
			{
				Text+="IP V6";
			}
			break;

			default:
			{
				Text+="UNKNOWN";
			}
			break;
		}

		return(Text);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline bool IsValidValue(ESocketDNSAddressType Value)
	{
		switch(Value)
		{
			case ESocketDNSAddressType::E_UNSPECIFIED:
			case ESocketDNSAddressType::E_IP_V4:
			case ESocketDNSAddressType::E_IP_V6:
			{
				return(true);
			}

			default:
			{
				return(true);
			}
		}

		return(false);
	}
//----------------------------------------------------------------------------------------------------------------------
	inline bool ParseValue(const CString& TextValue, ESocketDNSAddressType& Value)
	{
		CString													AdjustedTextValue=CString::ToUpperString(TextValue);

		if (AdjustedTextValue=="UNSPECIFIED")
		{
			Value=ESocketDNSAddressType::E_UNSPECIFIED;

			return(true);
		}
		else if (AdjustedTextValue=="IP_V4")
		{
			Value=ESocketDNSAddressType::E_IP_V4;

			return(true);
		}
		else if (AdjustedTextValue=="IP_V6")
		{
			Value=ESocketDNSAddressType::E_IP_V6;

			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
