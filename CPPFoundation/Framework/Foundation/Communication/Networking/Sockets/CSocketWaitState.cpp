//----------------------------------------------------------------------------------------------------------------------
#include "CSocketWaitState.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CSocketWaitState::CSocketWaitState(CSPSocket Socket, ESocketWaitFlags WaitFlags)
		: MSocket(Socket), MWaitFlags(WaitFlags), MWaitResultFlags(ESocketWaitResultFlags::E_NONE)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CSocketWaitState::~CSocketWaitState(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CSPSocket CSocketWaitState::GetSocket(void) const noexcept
	{
		return(MSocket);
	}
//----------------------------------------------------------------------------------------------------------------------
	ESocketWaitFlags CSocketWaitState::GetWaitFlags(void) const noexcept
	{
		return(MWaitFlags);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSocketWaitState::SetWaitFlags(ESocketWaitFlags Flags) noexcept
	{
		MWaitFlags=Flags;
	}
//----------------------------------------------------------------------------------------------------------------------
	ESocketWaitResultFlags CSocketWaitState::GetWaitResultFlags(void) const noexcept
	{
		return(MWaitResultFlags);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSocketWaitState::SetWaitResultFlags(ESocketWaitResultFlags Flags) noexcept
	{
		MWaitResultFlags=Flags;
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
