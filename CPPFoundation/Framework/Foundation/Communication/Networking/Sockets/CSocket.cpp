//----------------------------------------------------------------------------------------------------------------------
#include "CSocket.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CSocket::CSocket(CLogWriter& LogWriter, const CSocketID& SocketID)
		: MLogWriter(LogWriter), MLogCategorySocket(CString::Format("SOCKET [",SocketID,"]")), MSocketID(SocketID)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CSocket::~CSocket(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CLogWriter& CSocket::GetLogWriter(void) const noexcept
	{
		return(MLogWriter);
	}
//----------------------------------------------------------------------------------------------------------------------
	const CLogCategory& CSocket::GetLogCategorySocket(void) const noexcept
	{
		return(MLogCategorySocket);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CString CSocket::ToString(void) const
	{
		return(CString::Format("SOCKET [",GetSocketID(),"]"));
	}
//----------------------------------------------------------------------------------------------------------------------
	const CSocketID& CSocket::GetSocketID(void) const noexcept
	{
		return(MSocketID);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
