//----------------------------------------------------------------------------------------------------------------------
#include "CParserConfiguration.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CParserConfiguration::CParserConfiguration(const CString& ParserName)
		: MParserName(ParserName)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserConfiguration::CParserConfiguration(const CParserConfiguration& Other)
		: MParserName(Other.MParserName)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserConfiguration::CParserConfiguration(CParserConfiguration&& Other)
		: MParserName(FUNCTION_MOVE(Other.MParserName))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserConfiguration::~CParserConfiguration(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CParserConfiguration& CParserConfiguration::operator=(const CParserConfiguration& Other)
	{
		MParserName=Other.MParserName;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserConfiguration& CParserConfiguration::operator=(CParserConfiguration&& Other)
	{
		MParserName=FUNCTION_MOVE(Other.MParserName);

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	const CString& CParserConfiguration::GetParserName(void) const noexcept
	{
		return(MParserName);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
