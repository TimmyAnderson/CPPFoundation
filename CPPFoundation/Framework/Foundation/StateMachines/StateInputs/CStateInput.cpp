//----------------------------------------------------------------------------------------------------------------------
#include "CStateInput.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CStateInput::CStateInput(void)
		: MStateMachineID(CID::CreateEmptyIDAsString())
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput::CStateInput(const CStateMachineID& StateMachineID)
		: MStateMachineID(StateMachineID)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput::CStateInput(CStateMachineID&& StateMachineID)
		: MStateMachineID(FUNCTION_MOVE(StateMachineID))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput::CStateInput(const CStateInput& Other)
		: MStateMachineID(Other.MStateMachineID)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput::CStateInput(CStateInput&& Other) noexcept
		: MStateMachineID(FUNCTION_MOVE(Other.MStateMachineID))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput::~CStateInput(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CStateInput& CStateInput::operator=(const CStateInput& Other)
	{
		MStateMachineID=Other.MStateMachineID;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput& CStateInput::operator=(CStateInput&& Other) noexcept
	{
		MStateMachineID=FUNCTION_MOVE(Other.MStateMachineID);

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	bool CStateInput::HasStateMachineID(void) const
	{
		if (MStateMachineID.GetID().IsEmpty()==false)
		{
			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
	const CStateMachineID& CStateInput::GetStateMachineID(void) const
	{
		return(MStateMachineID);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
