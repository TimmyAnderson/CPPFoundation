//----------------------------------------------------------------------------------------------------------------------
#include "CEventContext.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CEventContext::CEventContext(const CEventID& EventID)
		: MEventID(EventID)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CEventContext::CEventContext(CEventID&& EventID)
		: MEventID(FUNCTION_MOVE(EventID))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CEventContext::CEventContext(const CEventContext& Other)
		: MEventID(Other.MEventID)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CEventContext::CEventContext(CEventContext&& Other) noexcept
		: MEventID(FUNCTION_MOVE(Other.MEventID))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CEventContext::~CEventContext(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CEventContext& CEventContext::operator=(const CEventContext& Other)
	{
		MEventID=Other.MEventID;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	CEventContext& CEventContext::operator=(CEventContext&& Other) noexcept
	{
		MEventID=FUNCTION_MOVE(Other.MEventID);

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	const CEventID& CEventContext::GetEventID(void) const noexcept
	{
		return(MEventID);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
