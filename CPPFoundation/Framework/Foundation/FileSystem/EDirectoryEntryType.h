//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <Framework/Foundation/Strings/CString.h>
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	enum class EDirectoryEntryType : uint8
	{
//----------------------------------------------------------------------------------------------------------------------
		E_DIRECTORY=1,
		E_FILE=2,
//----------------------------------------------------------------------------------------------------------------------
	};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline CString& operator<<(CString& Text, EDirectoryEntryType Value)
	{
		switch(Value)
		{
			case EDirectoryEntryType::E_DIRECTORY:
			{
				Text+="DIRECTORY";
			}
			break;

			case EDirectoryEntryType::E_FILE:
			{
				Text+="FILE";
			}
			break;

			default:
			{
				Text+="UNKNOWN";
			}
			break;
		}

		return(Text);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline bool IsValidValue(EDirectoryEntryType Value)
	{
		switch(Value)
		{
			case EDirectoryEntryType::E_DIRECTORY:
			case EDirectoryEntryType::E_FILE:
			{
				return(true);
			}

			default:
			{
				return(false);
			}
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------