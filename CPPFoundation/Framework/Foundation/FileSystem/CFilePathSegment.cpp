//----------------------------------------------------------------------------------------------------------------------
#include "CFilePathSegment.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	CFilePathSegment::CFilePathSegment(EFilePathSegmentType SegmentType, const CString& Segment)
		: MSegmentType(SegmentType), MSegment(Segment)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CFilePathSegment::CFilePathSegment(EFilePathSegmentType SegmentType, CString&& Segment)
		: MSegmentType(SegmentType), MSegment(FUNCTION_MOVE(Segment))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CFilePathSegment::~CFilePathSegment(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	bool CFilePathSegment::operator==(const CFilePathSegment& Other) const
	{
		if (MSegmentType==Other.GetSegmentType() && MSegment==Other.MSegment)
		{
			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CFilePathSegment::operator!=(const CFilePathSegment& Other) const
	{
		if (MSegmentType!=Other.GetSegmentType() || MSegment!=Other.MSegment)
		{
			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	EFilePathSegmentType CFilePathSegment::GetSegmentType(void) const noexcept
	{
		return(MSegmentType);
	}
//----------------------------------------------------------------------------------------------------------------------
	CString CFilePathSegment::GetSegment(void) const noexcept
	{
		return(MSegment);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
