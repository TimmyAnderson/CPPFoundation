//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <Framework/Foundation/Strings/CString.h>
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	enum class EDayOfWeek : uint8
	{
//----------------------------------------------------------------------------------------------------------------------
		E_UNKNOWN=0,
//----------------------------------------------------------------------------------------------------------------------
		E_MONDAY=1,
		E_TUESDAY=2,
		E_WEDNESDAY=3,
		E_THURSDAY=4,
		E_FRIDAY=5,
		E_SATURDAY=6,
		E_SUNDAY=7,
//----------------------------------------------------------------------------------------------------------------------
	};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline CString& operator<<(CString& Text, EDayOfWeek Value)
	{
		switch(Value)
		{
			case EDayOfWeek::E_MONDAY:
			{
				Text+="MONDAY";
			}
			break;

			case EDayOfWeek::E_TUESDAY:
			{
				Text+="TUESDAY";
			}
			break;

			case EDayOfWeek::E_WEDNESDAY:
			{
				Text+="WEDNESDAY";
			}
			break;

			case EDayOfWeek::E_THURSDAY:
			{
				Text+="THURSDAY";
			}
			break;

			case EDayOfWeek::E_FRIDAY:
			{
				Text+="FRIDAY";
			}
			break;

			case EDayOfWeek::E_SATURDAY:
			{
				Text+="SATURDAY";
			}
			break;

			case EDayOfWeek::E_SUNDAY:
			{
				Text+="SUNDAY";
			}
			break;

			case EDayOfWeek::E_UNKNOWN:
			{
				Text+="UNKNOWN";
			}
			break;

			default:
			{
				Text+="UNKNOWN";
			}
			break;
		}

		return(Text);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline bool IsValidValue(EDayOfWeek Value)
	{
		if (Value>=EDayOfWeek::E_MONDAY && Value<=EDayOfWeek::E_SUNDAY)
		{
			return(true);
		}
		else
		{
			return(false);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
