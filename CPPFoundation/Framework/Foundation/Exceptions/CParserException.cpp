//----------------------------------------------------------------------------------------------------------------------
#include "CParserException.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	using namespace std;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(void)
		: CException("")
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(const CString& Message)
		: CException(Message)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(CString&& Message)
		: CException(FUNCTION_MOVE(Message))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(const exception& Exception)
		: CException(Exception)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(const CString& Message, const exception& Exception)
		: CException(Message,Exception)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(const CString& Message, const CException& Exception)
		: CException(Message,Exception)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(const CParserException& Other)
		: CException(Other)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::CParserException(CParserException&& Other) noexcept
		: CException(FUNCTION_MOVE(Other))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException::~CParserException(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CParserException& CParserException::operator=(const CParserException& Other)
	{
		CException::operator=(Other);

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	CParserException& CParserException::operator=(CParserException&& Other) noexcept
	{
		CException::operator=(FUNCTION_MOVE(Other));

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
