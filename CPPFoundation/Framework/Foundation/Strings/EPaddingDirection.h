//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	enum class EPaddingDirection : uint8
	{
//----------------------------------------------------------------------------------------------------------------------
		E_LEFT=1,
		E_INTERNAL=2,
		E_RIGHT=3
//----------------------------------------------------------------------------------------------------------------------
	};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline bool CheckValue(EPaddingDirection PaddingDirection)
	{
		switch(PaddingDirection)
		{
			case EPaddingDirection::E_LEFT:
			case EPaddingDirection::E_INTERNAL:
			case EPaddingDirection::E_RIGHT:
			{
				return(true);
			}

			default:
			{
				return(false);
			}
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
