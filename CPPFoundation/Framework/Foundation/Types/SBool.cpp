//----------------------------------------------------------------------------------------------------------------------
#include "SBool.h"
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Foundation
{
//----------------------------------------------------------------------------------------------------------------------
	SBool::SBool(void)
		: MValue(false)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	SBool::SBool(bool Value)
		: MValue(Value)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	SBool::~SBool(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	SBool::operator bool(void) const noexcept
	{
		return(MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	SBool& SBool::operator=(const SBool& Other) noexcept
	{
		MValue=Other.MValue;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	SBool& SBool::operator=(bool Other) noexcept
	{
		MValue=Other;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	bool SBool::operator==(const SBool& Other) const noexcept
	{
		return(MValue==Other.MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool SBool::operator==(bool Other) const noexcept
	{
		return(MValue==Other);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool SBool::operator!=(const SBool& Other) const noexcept
	{
		return(MValue!=Other.MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool SBool::operator!=(bool Other) const noexcept
	{
		return(MValue!=Other);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	bool SBool::GetValue(void) const noexcept
	{
		return(MValue);
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
