//----------------------------------------------------------------------------------------------------------------------
	CSchedulerTime::CSchedulerTime(void)
		: MDateTime()
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CSchedulerTime::CSchedulerTime(const CDateTime& DateTime)
		: MDateTime(DateTime)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CSchedulerTime::~CSchedulerTime(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	bool CSchedulerTime::operator==(const CSchedulerTime& Other) const
	{
		return(MDateTime==Other.MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CSchedulerTime::operator!=(const CSchedulerTime& Other) const
	{
		return(MDateTime!=Other.MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CSchedulerTime::operator<(const CSchedulerTime& Other) const
	{
		return(MDateTime<Other.MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CSchedulerTime::operator<=(const CSchedulerTime& Other) const
	{
		return(MDateTime<=Other.MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CSchedulerTime::operator>(const CSchedulerTime& Other) const
	{
		return(MDateTime>Other.MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
	bool CSchedulerTime::operator>=(const CSchedulerTime& Other) const
	{
		return(MDateTime>=Other.MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CTimeout operator-(const CSchedulerTime& Left, const CSchedulerTime& Right)
	{
		CDateTime::DateTimeTicks								TimeDifferenceInTicks=(Left.GetDateTime()-Right.GetDateTime());
		CTimeout												TimeDifferenceInTimeout=CDateTime::ConvertTicksToTimeout(TimeDifferenceInTicks);

		return(TimeDifferenceInTimeout);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CString CSchedulerTime::ToString(void) const
	{
		return(CString::Format("SCHEDULER TIME [",MDateTime,"]"));
	}
//----------------------------------------------------------------------------------------------------------------------
	const CDateTime& CSchedulerTime::GetDateTime(void) const noexcept
	{
		return(MDateTime);
	}
//----------------------------------------------------------------------------------------------------------------------
