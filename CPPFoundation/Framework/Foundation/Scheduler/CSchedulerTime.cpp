	{
		MDateTime.AddYears(Years);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSchedulerTime::AddMonths(int32 Months)
	{
		MDateTime.AddMonths(Months);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSchedulerTime::AddDays(int32 Days)
	{
		MDateTime.AddDays(Days);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSchedulerTime::AddHours(int64 Hours)
	{
		MDateTime.AddHours(Hours);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSchedulerTime::AddMinutes(int64 Minutes)
	{
		MDateTime.AddMinutes(Minutes);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSchedulerTime::AddSeconds(int64 Seconds)
	{
		MDateTime.AddSeconds(Seconds);
	}
//----------------------------------------------------------------------------------------------------------------------
	void CSchedulerTime::AddMilliseconds(int64 Milliseconds)
	{
		MDateTime.AddMilliseconds(Milliseconds);
	}
