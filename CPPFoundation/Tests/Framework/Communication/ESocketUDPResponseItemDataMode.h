//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <iostream>
#include <Framework/Foundation/Types/Types.h>
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Testing
{
//----------------------------------------------------------------------------------------------------------------------
	enum class ESocketUDPResponseItemDataMode : uint8
	{
//----------------------------------------------------------------------------------------------------------------------
		E_DATA=1,
		E_USE_REQUEST_DATA=2,
		E_USE_VALUE_GENERATOR=3,
//----------------------------------------------------------------------------------------------------------------------
	};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline std::ostream& operator<<(std::ostream& Stream, ESocketUDPResponseItemDataMode Value)
	{
		switch(Value)
		{
			case ESocketUDPResponseItemDataMode::E_DATA:
			{
				Stream << "DATA";
			}
			break;

			case ESocketUDPResponseItemDataMode::E_USE_REQUEST_DATA:
			{
				Stream << "USE REQUEST DATA";
			}
			break;

			case ESocketUDPResponseItemDataMode::E_USE_VALUE_GENERATOR:
			{
				Stream << "USE VALUE GENERATOR";
			}
			break;

			default:
			{
				Stream << "UNKNOWN";
			}
			break;
		}

		return(Stream);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline bool IsValidValue(ESocketUDPResponseItemDataMode Value)
	{
		switch(Value)
		{
			case ESocketUDPResponseItemDataMode::E_DATA:
			case ESocketUDPResponseItemDataMode::E_USE_REQUEST_DATA:
			case ESocketUDPResponseItemDataMode::E_USE_VALUE_GENERATOR:
			{
				return(true);
			}

			default:
			{
				return(false);
			}
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
