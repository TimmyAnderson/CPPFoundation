//----------------------------------------------------------------------------------------------------------------------
#pragma once
//----------------------------------------------------------------------------------------------------------------------
#include <iostream>
#include <Framework/Foundation/Types/Types.h>
//----------------------------------------------------------------------------------------------------------------------
namespace CPPFoundation
{
//----------------------------------------------------------------------------------------------------------------------
namespace Testing
{
//----------------------------------------------------------------------------------------------------------------------
	enum class ESocketUDPResponseConditionDataComparisonMode : uint8
	{
//----------------------------------------------------------------------------------------------------------------------
		E_DATA=1,
		E_ANY_VALUE=2,
		E_ANY_VALUE_INCLUDING_NO_DATA=3,
//----------------------------------------------------------------------------------------------------------------------
	};
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline std::ostream& operator<<(std::ostream& Stream, ESocketUDPResponseConditionDataComparisonMode Value)
	{
		switch(Value)
		{
			case ESocketUDPResponseConditionDataComparisonMode::E_DATA:
			{
				Stream << "DATA";
			}
			break;

			case ESocketUDPResponseConditionDataComparisonMode::E_ANY_VALUE:
			{
				Stream << "ANY VALUE";
			}
			break;

			case ESocketUDPResponseConditionDataComparisonMode::E_ANY_VALUE_INCLUDING_NO_DATA:
			{
				Stream << "ANY VALUE INCLUDING NO DATA";
			}
			break;

			default:
			{
				Stream << "UNKNOWN";
			}
			break;
		}

		return(Stream);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	inline bool IsValidValue(ESocketUDPResponseConditionDataComparisonMode Value)
	{
		switch(Value)
		{
			case ESocketUDPResponseConditionDataComparisonMode::E_DATA:
			case ESocketUDPResponseConditionDataComparisonMode::E_ANY_VALUE:
			case ESocketUDPResponseConditionDataComparisonMode::E_ANY_VALUE_INCLUDING_NO_DATA:
			{
				return(true);
			}

			default:
			{
				return(false);
			}
		}
	}
//----------------------------------------------------------------------------------------------------------------------
}
//----------------------------------------------------------------------------------------------------------------------
}
