//----------------------------------------------------------------------------------------------------------------------
	CStateInput1::CStateInput1(const CString& Data)
		: MData(Data)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1::CStateInput1(const CStateMachineID& StateMachineID, const CString& Data)
		: CStateInput(StateMachineID), MData(Data)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1::CStateInput1(CStateMachineID&& StateMachineID, const CString& Data)
		: CStateInput(FUNCTION_MOVE(StateMachineID)), MData(Data)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1::CStateInput1(const CStateInput1& Other)
		: CStateInput(Other), MData(Other.MData)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1::CStateInput1(CStateInput1&& Other) noexcept
		: CStateInput(FUNCTION_MOVE(Other)), MData(FUNCTION_MOVE(Other.MData))
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1::~CStateInput1(void)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1& CStateInput1::operator=(const CStateInput1& Other)
	{
		CStateInput::operator=(Other);

		MData=Other.MData;

		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
	CStateInput1& CStateInput1::operator=(CStateInput1&& Other) noexcept
	{
		CStateInput::operator=(FUNCTION_MOVE(Other));

		MData=FUNCTION_MOVE(Other.MData);
		
		return(*this);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	const CString& CStateInput1::GetData(void) const noexcept
	{
		return(MData);
	}
