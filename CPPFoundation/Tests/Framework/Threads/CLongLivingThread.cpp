	using namespace CPPFoundation::Foundation;
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	CLongLivingThread::CLongLivingThread(CLogWriter& LogWriter)
		: CThread(LogWriter,"LONG LIVING THREAD"), MCounter(0)
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CLongLivingThread::~CLongLivingThread(void)
	{
		StopThread();
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	void CLongLivingThread::ThreadMethod(bool)
	{
		CConsoleHelper::WriteLine(CString::Format("THREAD is RUNNING [",++MCounter,"] !"));
	}
//----------------------------------------------------------------------------------------------------------------------
	CTimeout CLongLivingThread::GetTimeout(void)
	{
		return(CTimeout(500));
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	void CLongLivingThread::ThreadSafeUninitializationEvent(void)
	{
		MCounter=0;
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	void CLongLivingThread::StartThread(void)
	{
		InternalStartThread();
	}
//----------------------------------------------------------------------------------------------------------------------
	void CLongLivingThread::StopThread(void)
	{
		InternalStopThread();
	}
