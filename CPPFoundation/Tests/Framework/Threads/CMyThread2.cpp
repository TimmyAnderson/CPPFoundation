	CMyThread2::CMyThread2(CLogWriter& LogWriter)
		: CThread(LogWriter,"MY THREAD 2")
	{
	}
//----------------------------------------------------------------------------------------------------------------------
	CMyThread2::~CMyThread2(void)
	{
		StopThread(10000);
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	void CMyThread2::ThreadMethod(bool)
	{
		// LONG RUNNING THREAD.
		for(Size Index=0;Index<10;Index++)
		{
			CConsoleHelper::WriteLine("THREAD is RUNNING !");

			Sleep(1000);
		}
	}
//----------------------------------------------------------------------------------------------------------------------
	CTimeout CMyThread2::GetTimeout(void)
	{
		return(CTimeout(1000));
	}
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
//----------------------------------------------------------------------------------------------------------------------
	void CMyThread2::StartThread(void)
	{
		InternalStartThread();
	}
//----------------------------------------------------------------------------------------------------------------------
	void CMyThread2::StopThread(uint64 StopTimeoutInMS)
	{
		InternalStopThread(nullptr,StopTimeoutInMS);
	}
